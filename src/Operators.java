public class Operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        
        // Arithmetic Operators
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        //Comparison Operators
        System.out.println(10>5);
        System.out.println(10<5);
        System.out.println(10==5);
        System.out.println(10!=5);

        // Logical Operators
        System.out.println(true && false);
        System.out.println(true || false);
        System.out.println(!true);
    }
}
